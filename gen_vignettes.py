#!/usr/bin/env python3
"""Generate 9 ASCII art vignettes for Persekutuan Keris (Bab 11-25).
Every line inside each vignette is exactly 70 characters wide.
Border: +--...--+ (70 chars), Content: |...(68 chars)...|
"""

import sys

def border():
    return '+' + '-' * 68 + '+'

def row(content):
    """Pad or truncate content to exactly 68 chars, wrap with |...|"""
    if len(content) > 68:
        content = content[:68]
    return '|' + content.ljust(68) + '|'

def emit(lines):
    """Emit a complete 25-line vignette (1 top border + 23 content + 1 bottom border)."""
    assert len(lines) == 23, f"Expected 23 content lines, got {len(lines)}"
    result = [border()]
    for line in lines:
        result.append(row(line))
    result.append(border())
    assert len(result) == 25
    for r in result:
        assert len(r) == 70, f"Line length {len(r)}: {repr(r)}"
    return result


